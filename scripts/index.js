
$(document).ready(function(){
    $('.slider_container').slick({
        arrows: false,
        dots: true, 
    });

    $('.slider_container_2').slick({
        slidesToShow: 3,
        slidesToScroll: 1,
        dots: true, 
        arrows: true, 
        autoplay: true,
        autoplaySpeed: 4000,
    });

    $("a[href^='#']").click(function(e) {
        e.preventDefault();
        
        let position = $($(this).attr("href")).offset().top;
    
        $("body, html").animate({
            scrollTop: position
        }, 1000 );
    });
});

